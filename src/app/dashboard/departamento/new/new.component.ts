import { Component, OnInit,Input, AfterViewInit,Output,EventEmitter } from '@angular/core';
import {Departamento} from '../departamento.modelo';
import {DepartamentoService} from '../../../services/departamento.service';
import {LoginService} from '../../../services/login.service';
import swal from 'sweetalert2';

@Component({
  selector: 'app-new',
  templateUrl: './new.component.html'
})
export class NewComponent implements OnInit {
@Output() ready = new EventEmitter<any>();
public departamento: Departamento;
public errorMessage;
public respuesta;

constructor(
  private _DepartamentoService: DepartamentoService,
  private _loginService: LoginService,
  ){}

  ngOnInit() {
    this.departamento = new Departamento(null,null,null);
  }
  onCancelar(){
    this.ready.emit(true);
  }
  onEnviar(){
    let token = this._loginService.getToken();

		this._DepartamentoService.register(this.departamento,token).subscribe(
			response => {
        this.respuesta = response;
        console.log(this.respuesta);
        if(this.respuesta.status == 'success'){
          this.ready.emit(true);
          swal({
            title: 'Echo!',
            text: 'El registro se ha registrado con exito',
            type: 'success',
            confirmButtonText: 'Aceptar'
          })
        }else{
          swal({
            title: 'Error!',
            text: 'El departamento '+ this.departamento.nombre +' ya se encuentra registrado',
            type: 'error',
            confirmButtonText: 'Aceptar'
          })
        }
			error => {
					this.errorMessage = <any>error;

					if(this.errorMessage != null){
						console.log(this.errorMessage);
						alert("Error en la petici√≥n");
					}
				}

		}); 
  }

}